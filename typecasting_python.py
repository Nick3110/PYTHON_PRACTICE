print(int(x)+int(y))#output will be 57 
# above code is explicit typecasting. 
# explicit typecasting means user/coder does it. 
# implicit typecasting means python env. does it irrespective of user's choice
# basically in Python order of datatypr is not same hence while performing any function it convert output according to higher order datatype 
# eg. in float & Int, float have higher order hence while performing addition between them output will be float
